  # Introduction
  This report provides an analysis of educational performance trends among children aged 3 and 4, based on the 2019 Zimbabwe MICS6 survey.

# Methods
The analysis focused on four key educational areas: Literacy + Math, Physical, Learning, and Socio-emotional development.

